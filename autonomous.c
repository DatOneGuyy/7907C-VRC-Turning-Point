void pre_auton() {
	bStopTasksBetweenModes = true;

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;
	motor[armleft] = 0;
	motor[armright] = 0;
	motor[claw] = 0;

	SensorValue[encoderleft] = 0;
	SensorValue[encoderright] = 0;

}
int l;
void moveForward(int clicks, int speed) {
	SensorValue[encoderleft] = 0;
	SensorValue[encoderright] = 0;
	
	float kp = 0.15;
	float ki = 0.23;
	
	int rightpower = speed;
	int leftpower;
	
	int error = 0;
	int prevError = 0;
	int integral = 0;
	
	while (SensorValue[encoderright] < clicks) {
		datalogDataGroupStart();
		datalogAddValue(1, error);
		datalogAddValue(2, integral);
		datalogAddValue(3, derivative);
		datalogDataGroupEnd();
		error = SensorValue[encoderright] - abs(SensorValue[encoderleft]);
		integral += error;
		
		leftpower = rightpower + (error * kp) + (integral * ki);
		motor[frontleft] = leftpower;
		motor[frontright] = rightpower;
		motor[backleft] = leftpower;
		motor[backright] = rightpower;
		
		prevError = error;
		wait1Msec(15);
		
		errorValue = error;
		integralValue = integral;
		derivativeValue = derivative;
	}
	
	rightpower = 0;
	leftpower = 0;
}

void turnRight(int degrees, int speed) {
	int degrees10 = degrees * 4;

	SensorValue[encoderleft] = 0;

	while (abs(SensorValue[encoderleft]) < degrees10) {
		motor[frontleft] = speed;
		motor[backleft] = -speed;
		motor[frontright] = speed;
		motor[backright] = -speed;
	}

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;

	wait1Msec(100);
}

void turnLeft (int degrees, int speed) {
	int degrees10 = degrees * 4;

	SensorValue[encoderright] = 0;

	while (abs(SensorValue[encoderright]) < degrees10) {
		motor[frontleft] = -speed;
		motor[frontright] = speed;
		motor[backleft] = -speed;
		motor[backright] = speed;
	}

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;

	wait1Msec(100);
}

void arm(bool direction, long time) {
	if (!direction) {
		motor[armleft] = 63;
		motor[armright] = 63;
		wait1Msec(time);
		motor[armleft] = 0;
		motor[armright] = 0;
	} else {
		motor[armleft] = -63;
		motor[armright] = -63;
		wait1Msec(time);
		motor[armleft] = 0;
		motor[armright] = 0;
	}
}

void flip(long time) {
	motor[claw] = 127;
	wait1Msec(time);
	motor[claw] =0;
}


