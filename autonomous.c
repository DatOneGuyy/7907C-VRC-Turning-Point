void moveForward(int clicks, int speed) {
	SensorValue[encoderleft] = 0;
	SensorValue[encoderright] = 0;
	
	float kp = 0.15;
	float ki = 0.23;
	
	int rightpower = speed;
	int leftpower;
	
	int error = 0;
	int prevError = 0;
	int integral = 0;
	
	while (SensorValue[encoderright] < clicks) {
		error = SensorValue[encoderright] - abs(SensorValue[encoderleft]);
		integral += error;
		
		leftpower = rightpower + (error * kp) + (integral * ki);
		motor[frontleft] = leftpower;
		motor[frontright] = rightpower;
		motor[backleft] = leftpower;
		motor[backright] = rightpower;
		
		prevError = error;
		wait1Msec(15);
	
	}
	
	stopDrive();
}

void turnRight(int degrees, int speed) {
	SensorValue[gyro] = 0;
	
	int ticks = degrees * 10;
	
	while (abs(SensorValue[gyro] * 0.98513889) < ticks * 0.6) {
		motor[frontleft] = speed;
		motor[frontright] = -speed;
		motor[backleft] = speed;
		motor[backright] = -speed;
	}

	while (abs(SensorValue[gyro] * 0.98513889) < ticks * 0.8 && abs(SensorValue[gyro] * 0.98513889) > ticks * 0.6) {
		motor[frontleft] = speed * 0.8;
		motor[frontright] = -speed * 0.8;
		motor[backleft] = speed * 0.8;
		motor[backright] = -speed * 0.8;
	}
	
	while (abs(SensorValue[gyro] * 0.98513889) < ticks && abs(SensorValue[gyro] * 0.98513889) > ticks * 0.8) {
		motor[frontleft] = speed * 0.6;
		motor[frontright] = -speed * 0.6;
		motor[backleft] = speed * 0.6;
		motor[backright] = -speed * 0.6;
	}
	
	stopDrive();
	
}

void turnLeft (int degrees, int speed) {
	SensorValue[gyro] = 0;

	int ticks = degrees * 10;
	
	while (abs(SensorValue[gyro] * 0.98513889) < ticks * 0.6) {
		motor[frontleft] = -speed;
		motor[frontright] = speed;
		motor[backleft] = -speed;
		motor[backright] = speed;
	}

	while (abs(SensorValue[gyro] * 0.98513889) < ticks * 0.8 && abs(SensorValue[gyro] * 0.98513889) > ticks * 0.6) {
		motor[frontleft] = -speed * 0.5;
		motor[frontright] = speed * 0.5;
		motor[backleft] = -speed * 0.5;
		motor[backright] = speed * 0.5;
	}
	
	while (abs(SensorValue[gyro] * 0.98513889) < ticks && abs(SensorValue[gyro] * 0.98513889) > ticks * 0.8) {
		motor[frontleft] = -speed * 0.3;
		motor[frontright] = speed * 0.3;
		motor[backleft] = -speed * 0.3;
		motor[backright] = speed * 0.3;
	}
	
	stopDrive();
}

void arm(bool direction, long time) {
	if (!direction) {
		motor[armleft] = 63;
		motor[armright] = 63;
		wait1Msec(time);
		motor[armleft] = 0;
		motor[armright] = 0;
	} else {
		motor[armleft] = -63;
		motor[armright] = -63;
		wait1Msec(time);
		motor[armleft] = 0;
		motor[armright] = 0;
	}
}

void flip(long time) {
	motor[claw] = 127;
	wait1Msec(time);
	motor[claw] =0;
}


