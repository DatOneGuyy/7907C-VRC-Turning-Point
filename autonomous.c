void pre_auton() {
	bStopTasksBetweenModes = true;

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;
	motor[armleft] = 0;
	motor[armright] = 0;
	motor[claw] = 0;

	SensorValue[encoderleft] = 0;
	SensorValue[encoderright] = 0;

}

void moveForward(float rotations, int speed) {
	SensorValue[encoderleft] = 0;
	SensorValue[encoderright] = 0;

	while (abs(SensorValue[encoderleft]) < rotations * 360) {
		motor[frontleft] = speed;
		motor[frontright] = speed;
		motor[backleft] = speed;
		motor[backright] = speed;
	}

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;

	wait1Msec(100);
}

void turnRight(int degrees, int speed) {
	int degrees10 = degrees * 4;

	SensorValue[encoderleft] = 0;

	while (abs(SensorValue[encoderleft]) < degrees10) {
		motor[frontleft] = speed;
		motor[backleft] = -speed;
		motor[frontright] = speed;
		motor[backright] = -speed;
	}

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;

	wait1Msec(100);
}

void turnLeft (int degrees, int speed) {
	int degrees10 = degrees * 4;

	SensorValue[encoderright] = 0;

	while (abs(SensorValue[encoderright]) < degrees10) {
		motor[frontleft] = -speed;
		motor[frontright] = speed;
		motor[backleft] = -speed;
		motor[backright] = speed;
	}

	motor[frontleft] = 0;
	motor[frontright] = 0;
	motor[backleft] = 0;
	motor[backright] = 0;

	wait1Msec(100);
}

void arm(bool direction, long time) {
	if (!direction) {
		motor[armleft] = 63;
		motor[armright] = 63;
		wait1Msec(time);
		motor[armleft] = 0;
		motor[armright] = 0;
	} else {
		motor[armleft] = -63;
		motor[armright] = -63;
		wait1Msec(time);
		motor[armleft] = 0;
		motor[armright] = 0;
	}
}

void flip(long time) {
	motor[claw] = 127;
	wait1Msec(time);
	motor[claw] =0;
}


